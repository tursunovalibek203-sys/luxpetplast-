// Initialize language system
document.addEventListener('DOMContentLoaded', function() {
    console.log('Language system loading...');
    
    // Get saved language or default to 'uz'
    let currentLang = localStorage.getItem('language') || 'uz';
    console.log('Current language:', currentLang);
    
    // Apply language
    applyLanguage(currentLang);
    
    // Add event listeners to language buttons
    const langButtons = document.querySelectorAll('.lang-btn');
    console.log('Found language buttons:', langButtons.length);
    
    langButtons.forEach((btn, index) => {
        console.log(`Button ${index}:`, btn.getAttribute('data-lang'));
        btn.addEventListener('click', function(e) {
            console.log('Button clicked!', this.getAttribute('data-lang'));
            const lang = this.getAttribute('data-lang');
            switchLanguage(lang);
        });
    });
    
    console.log('Language system ready!');
});

// Switch language
function switchLanguage(lang) {
    console.log('Switching to:', lang);
    
    // Update active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
    
    // Save language
    localStorage.setItem('language', lang);
    
    // Apply translations
    applyLanguage(lang);
}

// Apply language translations
function applyLanguage(lang) {
    console.log('Applying language:', lang);
    
    const langData = translations[lang] || translations.uz;
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (langData[key]) {
            element.textContent = langData[key];
        }
    });
    
    console.log('Language applied successfully!');
}
